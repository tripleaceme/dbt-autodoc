{% macro generate_descriptions(model_name=none) %}
    {#
        Generates draft YAML descriptions for dbt models using heuristic inference.

        Usage (print to terminal):
            dbt run-operation generate_descriptions
            dbt run-operation generate_descriptions --args '{model_name: my_model}'

        Usage (write to draft file):
            dbt run-operation generate_descriptions --args '{model_name: stg_orders}' 2>&1 \
              | sed -n '/^---AUTODOC-START---$/,/^---AUTODOC-END---$/{//d;p;}' \
              > models/staging/draft_stg_orders.yml
    #}

    {# Retrieve all nodes from the dbt graph #}
    {% set nodes = [] %}

    {% if model_name %}
        {# Single model mode #}
        {% for node_id, node in graph.nodes.items() %}
            {% if node.resource_type == 'model' and node.name == model_name %}
                {% do nodes.append(node) %}
            {% endif %}
        {% endfor %}

        {% if nodes | length == 0 %}
            {{ log("ERROR: Model '" ~ model_name ~ "' not found in project.", info=true) }}
            {{ return('') }}
        {% endif %}
    {% else %}
        {# All models mode #}
        {% for node_id, node in graph.nodes.items() %}
            {% if node.resource_type == 'model' and node.package_name == project_name %}
                {% do nodes.append(node) %}
            {% endif %}
        {% endfor %}
    {% endif %}

    {% set output_lines = [] %}
    {% do output_lines.append('# =============================================================================') %}
    {% if model_name %}
        {% do output_lines.append('# Draft descriptions for model: ' ~ model_name) %}
    {% else %}
        {% do output_lines.append('# Draft descriptions for all models') %}
    {% endif %}
    {% do output_lines.append('# Generated by dbt-autodoc (heuristic mode)') %}
    {% do output_lines.append('#') %}
    {% do output_lines.append('# Review these descriptions carefully before merging into your schema files.') %}
    {% do output_lines.append('# =============================================================================') %}
    {% do output_lines.append('') %}
    {% do output_lines.append('version: 2') %}
    {% do output_lines.append('') %}
    {% do output_lines.append('models:') %}

    {% for node in nodes | sort(attribute='name') %}
        {% set model_desc = dbt_autodoc.infer_model_description(node.name) | trim %}
        {% do output_lines.append('  - name: ' ~ node.name) %}
        {% do output_lines.append('    description: "' ~ model_desc ~ '"') %}

        {# Get columns from the database if the model is materialized #}
        {% set columns = adapter.get_columns_in_relation(ref(node.name)) %}

        {% if columns | length > 0 %}
            {% do output_lines.append('    columns:') %}
            {% for col in columns %}
                {% set col_desc = dbt_autodoc.infer_column_description(col.name, col.dtype) | trim %}
                {% do output_lines.append('      - name: ' ~ col.name) %}
                {% do output_lines.append('        description: "' ~ col_desc ~ '"') %}
            {% endfor %}
        {% endif %}

        {% do output_lines.append('') %}
    {% endfor %}

    {# Print with markers so output can be piped to a file cleanly #}
    {{ log('---AUTODOC-START---', info=true) }}
    {% for line in output_lines %}
        {{ log(line, info=true) }}
    {% endfor %}
    {{ log('---AUTODOC-END---', info=true) }}
{% endmacro %}
